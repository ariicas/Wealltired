package com.class08;

public class ReplWhile {
public static void main(String[]args) {
 for(int r=7;r>=1;r--) {
	for(int c=1; c<=r; c++) 
    {
				System.out.print(c+" ");
    }
    System.out.println();
    }
      for (int i=2; i<=7; i++)
      {
          for (int j=1; j<=i; j++)
          {
              System.out.print(j+" ");
          }
          System.out.println();
      }
		
	}
}
