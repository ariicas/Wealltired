package reviewclass4;

public class Recap1 {

	public static void main(String[] args) {
		
		int i=0;
		System.out.println("int i=10; \n"+i);
		i=i-20; //10
		System.out.println("int i=10; \n"+i);
		i-=10; //30
		System.out.println("int i=10; \n"+i);
		i--;
		System.out.println("int i=10; \n"+i);
		--i;
		
		System.out.println("--i"+(--i));
		System.out.println("i-- "+(i--));
		System.out.println("i "+i);
	}

}
